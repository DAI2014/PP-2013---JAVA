    private void jBadicionarLinhaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBadicionarLinhaActionPerformed
        if(jCtaxa.getSelectedItem()!= null && jTtotal.getText().length()!=0){
        Double total = Double.parseDouble(jTtotal.getText().toString());
        String taxaS = jCtaxa.getSelectedItem().toString();
        taxaS = taxaS.substring(0, taxaS.length() -1);
        Float taxa = Float.parseFloat(taxaS);
        FaturaLinha f = new FaturaLinha(total, taxa);
        listaLinhas.add(f);
        listaLinhas();
        } else {
            JOptionPane.showMessageDialog(this, "Por favor insira o total e iva correspondente","Inserção de linhas", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_jBadicionarLinhaActionPerformed

