    public InsereFatManual(EFactur sistema, Fatura f) {
        totalLinhas = 0;
        ivaLinhas = 0;
        baseTribLinhas = 0;
        initComponents();
        if(sistema.devolveComerciante(f.getIntrodPor())==null){
                jTnifConsumidor.setText(f.getNifReceptor().toString());
                jTnifConsumidor.setEnabled(false);
                jTnomeConsumidor.setText(f.getNome().toString());
                jTnomeConsumidor.setEnabled(false);
                jTnifComerciante.setText(f.getNifEmissor().toString());
                jTnumeroFatura.setText(f.getnFatura());
                ut = sistema.devolveComerciante(f.getIntrodPor());
                pack();
        } else{
                jTnifComerciante.setText(f.getNifEmissor().toString());
                jTnifComerciante.setEnabled(false);
                jTnomeConsumidor.setText(f.getNome().toString());
                jTnifConsumidor.setText(f.getNifReceptor().toString());
                jTnumeroFatura.setText(f.getnFatura().toString());
                ut = sistema.devolveConsumidor(f.getIntrodPor());
                pack();
        }
            
                
    }
    public void listaLinhas(){
            totalLinhas = 0;
            ivaLinhas = 0;
            baseTribLinhas = 0;
            DefaultTableModel model = new DefaultTableModel();
            model.setColumnIdentifiers(new String[] {"Total", "Taxa", "Iva", "Base Tributavel"});
            
            for (FaturaLinha l : listaLinhas){
            model.addRow(new String[] {String.valueOf(l.getTotalLinha()), String.valueOf(l.getTaxa()), String.valueOf(l.getIva()), String.valueOf(l.getBaseTrib())});
            totalLinhas += l.getTotalLinha();
            ivaLinhas += l.getIva();
            baseTribLinhas += l.getBaseTrib();
            }
            jTlinhas.setModel(model);
            if(totalLinhas != 0){
                jLtotalLinhas.setText(String.valueOf(df.format(totalLinhas)));
                jLivaLinhas.setText(String.valueOf(df.format(ivaLinhas)));
                jLbaseTribLinhas.setText(String.valueOf(df.format(baseTribLinhas)));
                pack();
            }
    }
    
    

