    private void jBremoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBremoverActionPerformed
        if(sistema.devolveComerciante(jTnif.getText())!= null){
            int resposta = JOptionPane.showConfirmDialog(this, "Tem a certeza que pretende remover este Comerciante", "Remove", JOptionPane.YES_NO_OPTION);
               if(resposta == JOptionPane.YES_NO_OPTION){
                   sistema.removeComerciante(sistema.devolveComerciante(jTnif.getText()));
        }
        }
        if(sistema.devolveConsumidor(jTnif.getText())!= null){
            int resposta = JOptionPane.showConfirmDialog(this, "Tem a certeza que pretende remover este Consumidor", "Remove", JOptionPane.YES_NO_OPTION);
               if(resposta == JOptionPane.YES_NO_OPTION){
                   sistema.removeConsumidor(sistema.devolveConsumidor(jTnif.getText()));
        }
        }
    }//GEN-LAST:event_jBremoverActionPerformed

