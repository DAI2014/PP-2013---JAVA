    private void jBadicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBadicionarActionPerformed
        if(jBadicionar.getText().toString().equals("Adicionar")){
        if("".equals(jTnome.getText())){
            JOptionPane.showMessageDialog(this, "Insira o nome","Novo Utilizador", JOptionPane.WARNING_MESSAGE);
        } else { if ("".equals(jTnif.getText())){
            JOptionPane.showMessageDialog(this, "Insira o nif","Novo Utilizador", JOptionPane.WARNING_MESSAGE);
        } else { if ("".equals(jTpassword.getText())){
            JOptionPane.showMessageDialog(this, "Insira a password","Novo Utilizador", JOptionPane.WARNING_MESSAGE);
        } else { if (jTnif.getText().length()!=9){
            JOptionPane.showMessageDialog(this, "Nif invalido","Novo Utilizador", JOptionPane.WARNING_MESSAGE);
        } else {
            if("Comerciante".equals(utilizador)){
                Comerciante cm = new Comerciante(jTnif.getText(), jTnome.getText(), jTmorada.getText(), jTtelefone.getText(), jTtelemovel.getText(), jTcc.getText(), jTpassword.getText(),cae );
                sistema.insereComerciante(cm);
                JOptionPane.showMessageDialog(this, "Novo Comerciante inserido com sucesso","Novo Utilizador", JOptionPane.WARNING_MESSAGE);
                dispose();
            } else{
                Consumidor cn = new Consumidor(jTnif.getText(), jTnome.getText(), jTmorada.getText(), jTtelefone.getText(), jTtelemovel.getText(), jTcc.getText(), jTpassword.getText());
                sistema.insereConsumidor(cn);
                JOptionPane.showMessageDialog(this, "Novo Consumidor inserido com sucesso","Novo Utilizador", JOptionPane.WARNING_MESSAGE);
                dispose();
            }
        }
        }
        }
        }
        
        } else if(jBadicionar.getText().toString().equals("Procurar")){
            if(sistema.devolveComerciante(jTnif.getText())!=null){
                Comerciante cm = sistema.devolveComerciante(jTnif.getText());
                jBfaturas.setVisible(true);
                jBeditar.setVisible(true);
                jBremover.setVisible(true);
                jBadicionar.setText("Gravar");
                pack();
                jTcc.setText(cm.getCc());
                jTmorada.setText(cm.getMorada());
                jTnome.setText(cm.getNome());
                jTpassword.setText(cm.getPassword());
                jTtelefone.setText(cm.getTelefone());
                jTtelemovel.setText(cm.getTelemovel());
                jCcae.setSelectedItem(cm.getCAE());
                pack();
                
            } else if(sistema.devolveConsumidor(jTnif.getText())!=null){
                Consumidor cn = sistema.devolveConsumidor(jTnif.getText());
                jBfaturas.setVisible(true);
                jBeditar.setVisible(true);
                jBremover.setVisible(true);
                jBadicionar.setText("Gravar");
                pack();
                jTcc.setText(cn.getCc());
                jTmorada.setText(cn.getMorada());
                jTnome.setText(cn.getNome());
                jTpassword.setText(cn.getPassword());
                jTtelefone.setText(cn.getTelefone());
                jTtelemovel.setText(cn.getTelemovel());
                pack();
                
                
            } else {
                JOptionPane.showMessageDialog(this, "Insira um NIF valido","Edicao de utiliador", JOptionPane.WARNING_MESSAGE);
            } 
            
            
        } else if(jBadicionar.getText().toString().equals("Gravar")){
            if(sistema.devolveComerciante(jTnif.getText())!=null){
                Comerciante cm = sistema.devolveComerciante(jTnif.getText());
                cm.setCc(jTcc.getText());
                cm.setMorada(jTmorada.getText());
                cm.setNome(jTnome.getText());
                cm.setPassword(jTpassword.getText());
                cm.setTelefone(jTtelefone.getText());
                cm.setTelemovel(jTtelemovel.getText());
                cm.setCae(jCcae.getSelectedItem().toString());
                
                
                
            } else if(sistema.devolveConsumidor(jTnif.getText())!=null){
                Consumidor cn = sistema.devolveConsumidor(jTnif.getText());
                cn.setCc(jTcc.getText());
                cn.setMorada(jTmorada.getText());
                cn.setNome(jTnome.getText());
                cn.setPassword(jTpassword.getText());
                cn.setTelefone(jTtelefone.getText());
                cn.setTelemovel(jTtelemovel.getText());
                
                
            }
            pack();
            dispose();
        }
            
        
        
 
    }//GEN-LAST:event_jBadicionarActionPerformed

